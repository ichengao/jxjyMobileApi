package cn.gc80.datamodel.sysbase;

/**
 * PK_SYS_CERT_PROVICE 证书区域对照表
 */
public class SysCertProvice {
	private String id;
	private String certNo;
	private SysProvince sysProvince;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getCertNo() {
		return certNo;
	}

	public void setCertNo(String certNo) {
		this.certNo = certNo;
	}

	public SysProvince getSysProvince() {
		return sysProvince;
	}

	public void setSysProvince(SysProvince sysProvince) {
		this.sysProvince = sysProvince;
	}
	
	

	public SysCertProvice() {
		
	}

	public SysCertProvice(String id, String certNo, SysProvince sysProvince) {
		super();
		this.id = id;
		this.certNo = certNo;
		this.sysProvince = sysProvince;
	}

	public SysCertProvice(String certNo) {
		super();
		this.certNo = certNo;
	}
	


}
