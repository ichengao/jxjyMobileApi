package cn.gc80.datamodel.sysbase;


public class SysCardTemplate implements java.io.Serializable {

	private static final long serialVersionUID = 8484993849070713409L;

	private String id;

	private String tempNo;

	private String tempName;

	private String tempType;

	private Long tempDenomination;
	
	private Long tempLimit;
	
	private String tempValidate;
	
	private Long price;

	private String delflag;

	// Constructors

	/** default constructor */
	public SysCardTemplate() {
	}

	/** minimal constructor */
	public SysCardTemplate(String tempName, String tempType,
			Long tempDenomination, Long price, String delflag) {
		this.tempName = tempName;
		this.tempType = tempType;
		this.tempDenomination = tempDenomination;
		this.price = price;
		this.delflag = delflag;
	}

	/** full constructor */
	public SysCardTemplate(String tempNo, String tempName, String tempType,
			Long tempDenomination, Long price, String delflag) {
		this.tempNo = tempNo;
		this.tempName = tempName;
		this.tempType = tempType;
		this.tempDenomination = tempDenomination;
		this.price = price;
		this.delflag = delflag;
	}
	public SysCardTemplate(String id, String tempNo, String tempName,
			Long tempDenomination, Long price, String tempType,
			String tempValidate) {
		this.id = id;
		this.tempNo = tempNo;
		this.tempName = tempName;
		this.tempDenomination = tempDenomination;
		this.price = price;
		this.tempType = tempType;
		this.tempValidate = tempValidate;
	}
	// Property accessors

	public String getId() {
		return this.id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTempNo() {
		return this.tempNo;
	}

	public void setTempNo(String tempNo) {
		this.tempNo = tempNo;
	}

	public String getTempName() {
		return this.tempName;
	}

	public void setTempName(String tempName) {
		this.tempName = tempName;
	}

	public String getTempType() {
		return this.tempType;
	}

	public void setTempType(String tempType) {
		this.tempType = tempType;
	}

	public Long getTempDenomination() {
		return this.tempDenomination;
	}

	public void setTempDenomination(Long tempDenomination) {
		this.tempDenomination = tempDenomination;
	}

	public Long getPrice() {
		return this.price;
	}

	public void setPrice(Long price) {
		this.price = price;
	}

	public String getDelflag() {
		return this.delflag;
	}

	public void setDelflag(String delflag) {
		this.delflag = delflag;
	}

	public String getTempValidate() {
		return tempValidate;
	}

	public void setTempValidate(String tempValidate) {
		this.tempValidate = tempValidate;
	}

	public static long getSerialVersionUID() {
		return serialVersionUID;
	}

	public Long getTempLimit() {
		return tempLimit;
	}

	public void setTempLimit(Long tempLimit) {
		this.tempLimit = tempLimit;
	}

}