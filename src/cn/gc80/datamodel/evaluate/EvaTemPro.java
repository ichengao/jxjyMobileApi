package cn.gc80.datamodel.evaluate;

/**
 * 
 * LRN_EVA_TEM_PRO（评价对象对应表）
 */
public class EvaTemPro implements java.io.Serializable {

	private String id;
	// 模版id
	private EvaTemplate EvaTemplate;
	//对象id 
	private String proId;
	// 类型
	private String type;
	// 状态
	private String status;
	// 删除标志
	private String delflag;

	/** default constructor */
	public EvaTemPro() {
	}


	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}


	public EvaTemplate getEvaTemplate() {
		return EvaTemplate;
	}


	public void setEvaTemplate(EvaTemplate evaTemplate) {
		EvaTemplate = evaTemplate;
	}

	

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getDelflag() {
		return delflag;
	}

	public void setDelflag(String delflag) {
		this.delflag = delflag;
	}

	public String getProId() {
		return proId;
	}

	public void setProId(String proId) {
		this.proId = proId;
	}

}
